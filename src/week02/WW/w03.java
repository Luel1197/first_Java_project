package week02.WW;

public class w03 {
    public static void main(String[] args) {
        System.out.println(10>9); //10이 9보다 크니?
        System.out.println(10>=9); //10이 9보다 크거나 작니?
        System.out.println(10<9); //10이 9보다 작니?
        System.out.println(10<=9); //10이 9보다 작거나 같니?
        System.out.println(10 !=10); //10은 10이 아니니?
    }
}
