package week02.WW;

public class w04 {
    public static void main(String[] args) {
        //논리 연산
        //비교 연산의 결과값으로 받을 수 있는 불리언 값을 연결하는 연살자
        //조건을 연결 하였을 때 불리언 값들을 조합하여 참 것짓을 출력한다.
        // && and, || or, ! not
        boolean flag1 = true;
        boolean flag2 = true;
        boolean flag3 = false;
        System.out.println(flag1);
        System.out.println(flag2);
        System.out.println(flag3);
        // 1 피연산자 중 하나라도 트로면 트로다
        System.out.println(!flag1);
    }
}
